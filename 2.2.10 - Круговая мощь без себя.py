'''
Считай целые числа через пробел.
Для каждого элемента выведи произведение всех остальных элементов этого списка.
Деление использовать нельзя. Вывод сделай в одну строку через пробел.
Если ввод содержит одно число, то вывести 1.
'''

# Считываем строку из ввода, разбиваем её по пробелам и преобразуем каждую часть в целое число
numbers = list(map(int, input().split()))

# Создаём пустой список для хранения результата — произведений всех элементов, кроме текущего
result = []

# Перебираем индексы списка numbers
for i in range(len(numbers)):
    # Делаем копию исходного списка, чтобы не изменять оригинал
    tmp = numbers.copy()
    # Удаляем из копии элемент с индексом i — оставляем только остальные числа
    tmp.pop(i)

    # Инициализируем переменную для хранения произведения
    reduce = 1
    # Перебираем все оставшиеся числа в tmp и умножаем их друг на друга
    for num in tmp:
        reduce *= num

    # Добавляем полученное произведение в результирующий список
    result.append(reduce)

# Выводим все элементы списка result через пробел
print(*result)