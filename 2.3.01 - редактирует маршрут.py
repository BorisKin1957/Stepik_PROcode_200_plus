'''
Нужно написать программу, которая редактирует маршрут по набору команд.

Первая строка входных данных содержит маршрут: элементы через запятую и пробел.
Например:

Страж-1, Мост, Пристань, Кордон, Перевал


Вторая строка содержит целое число q -- количество команд.
Далее идут q строк, каждая строка это одна команда одного из четырёх видов.

Все индексы в командах 0-based (первый элемент имеет индекс 0).

Команды:

1. INS i : value

Вставить значение value перед элементом с индексом i.

Пусть текущая длина маршрута равна n. Тогда индекс i нужно сначала ограничить
диапазоном от 0 до n:

если i < 0, считаем i = 0;

если i > n, считаем i = n (вставка в конец);

если 0 <= i <= n, оставляем i как есть.

После этого вставляем строку value перед элементом с индексом i (или в конец, если i = n).
Часть value это вся подстрока после последовательности символов " : " в команде
(может содержать пробелы).

2. DEL i

Удалить элемент с индексом i.

Пусть текущая длина маршрута равна n. Если 0 <= i < n, элемент с индексом i
удаляется, список сжимается.
Если i вне этого диапазона (меньше 0 или больше либо равен n), команда
игнорируется, маршрут не меняется.

3. MOV i j

Перенести элемент с индекса i перед индексом j.

Пусть текущая длина маршрута равна n.

Порядок действий:

Проверяем индекс i. Если i вне диапазона [0, n-1], команда полностью игнорируется
и ничего не меняется.

Если i корректен, берём элемент с индекса i, удаляем его из маршрута. Список
"сжимается", его новая длина становится n' = n - 1.

Теперь работаем с индексом j уже для сжатого списка длины n'. Индекс j нужно
ограничить диапазоном [0, n']:

если j < 0, считаем j = 0;

если j > n', считаем j = n';

если 0 <= j <= n', оставляем j как есть.

Вставляем ранее удалённый элемент перед элементом с индексом j (или в конец, если j = n').

 4. REP i : value

Заменить элемент по индексу i на строку value.

Пусть текущая длина маршрута равна n. Если 0 <= i < n, элемент с индексом i заменяется на value.
Если i вне диапазона [0, n-1], команда игнорируется.

Часть value это вся подстрока после " : " в команде.

После выполнения всех q команд нужно вывести итоговый маршрут:
каждый элемент маршрута -- на отдельной строке, в текущем порядке.
'''

# Считываем начальный маршрут и разбиваем по запятым, убираем лишние пробелы
route = list(map(str.strip, input().split(',')))

# Запоминаем текущую длину маршрута
n = len(route)

# Читаем количество команд
for _ in range(int(input())):
    # Считываем очередную команду
    string = input()

    # Проверяем, содержит ли команда символ ':' (актуально для INS и REP)
    if ':' in string:
        # Разделяем строку по символу ':', обрезаем пробелы
        string = list(map(str.strip, string.split(':')))
        # Первая часть — команда и индекс; разбиваем её
        tmp = string[0].split()
        cmd, idx, point = tmp[0], int(tmp[1]), string[1]
    else:
        # Если ':' нет, значит это DEL или MOV
        string = list(map(str.strip, string.split()))
        if len(string) == 3:
            # Команда с двумя числами: MOV i j
            cmd, idx, idx_ = string[0], int(string[1]), int(string[2])
        else:
            # Команда с одним числом: DEL i
            cmd, idx = string[0], int(string[1])

    # Обрабатываем команду с помощью match-case
    match cmd:
        case 'INS':
            # Ограничиваем индекс вставки:
            idx = min(max(0, idx), n)
            # Вставляем элемент по индексу или в конец
            if idx < n:
                route.insert(idx, point)
            else:
                route.append(point)
            # Увеличиваем длину маршрута
            n += 1

        case 'DEL':
            # Удаляем элемент, если индекс в допустимом диапазоне
            if 0 <= idx < n:
                route.pop(idx)
                n -= 1  # Уменьшаем длину

        case 'MOV':
            # Проверяем корректность индекса i
            if 0 <= idx < n:
                # Извлекаем элемент по индексу i
                new = route.pop(idx)
                n -= 1  # Длина уменьшилась после pop
                # Корректируем индекс j относительно новой длины
                idx_ = max(0, min(idx_, n))
                # Вставляем элемент на новое место
                if idx_ == n:
                    route.append(new)
                else:
                    route.insert(idx_, new)
                n += 1  # Длина восстановилась после вставки
            # Если idx некорректен — ничего не делаем

        case 'REP':
            # Заменяем элемент по индексу, если он допустим
            if 0 <= idx < n:
                route[idx] = point  # Используем point, а не string[1]

# Выводим итоговый маршрут: каждый элемент на отдельной строке
print(*route, sep='\n')