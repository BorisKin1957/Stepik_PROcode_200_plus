'''
Задание

Считай n. Далее n строк формата:

name x y z type hh:mm

где name --> без пробелов, x,y,z --> целые, type ∈ {BEACON, DRIFT, GHOST}, hh:mm -->
время с ведущими нулями (00–23:00–59).

Фильтрация и сортировка:

    Отбрось строки с некорректным type или временем.

    Для каждого name оставь ровно одну самую раннюю валидную запись по времени (hh,mm).
    При равенстве времени у этого же name --> сохраняй первую по вводу.

    Отсортируй оставшиеся по времени (hh,mm) по возрастанию, при равенстве --> по name.
    Выведи строки в формате: name@x,y,z|type|hh:mm.
'''

# Множество допустимых типов буёв
correct = {'BEACON', 'DRIFT', 'GHOST'}

# Список для хранения валидных записей
buoys = []

n = int(input())
for i in range(n):
    line = input().split()

    # Распаковываем данные: имя, координаты, тип, время
    name, x, y, z, tp, time_ = line

    # Преобразуем координаты в целые числа
    x, y, z = int(x), int(y), int(z)

    # Разделяем время на часы и минуты
    hh, mm = time_.split(':')

    # Преобразуем часы и минуты в целые числа
    hh, mm = int(hh), int(mm)

    # Проверяем корректность типа и времени
    if tp in correct and 0 <= hh <= 23 and 0 <= mm <= 59:
        # Добавляем запись с индексом ввода i (чтобы запомнить порядок поступления)
        buoys.append((i, name, x, y, z, tp, hh, mm))

# Cначала сортируем по имени, потом по времени, потом по индексу ввода
buoys.sort(key=lambda x: (x[1], x[6], x[7], x[0]))

unique = []  # Список имён, которые уже были добавлены
result = []  # Итоговые уникальные записи

# Проходим по отсортированным данным
for index, name, x, y, z, tp, hh, mm in buoys:
    if name not in unique:
        # Добавляем первое вхождение имени (самое раннее по времени и первое по вводу)
        unique.append(name)
        result.append((name, x, y, z, tp, hh, mm))

# Сортируем результат по времени (hh, mm), затем по имени
result.sort(key=lambda x: (x[5], x[6], x[0]))

# Выводим результат в требуемом формате
for name, x, y, z, tp, hh, mm in result:
    print(f'{name}@{x},{y},{z}|{tp}|{hh:02}:{mm:02}')