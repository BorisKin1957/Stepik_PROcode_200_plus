'''
Вход:
строка 1 -- массив целых чисел через пробел (может быть пустой);
строка 2 -- целое K.

Нужно посчитать количество непустых подмассивов (непрерывных фрагментов массива),
в которых встречается ровно K различных значений.

Выход: одно целое число.
Граничные случаи: если массив пустой или K ≤ 0, выведи 0.
'''

s, k = input(), int(input())

if not s or k <= 0:
    print(0)
    exit()

array = list(map(int, s.split()))
n = len(array)

result = 0

for i in range(n):
    for j in range(i, n + 1):
        if len(set(array[i:j])) == k:
            result += 1

print(result)


