'''
Считай n, затем n строк:

HH:MM type

(time неубывает). Затем считай K.

Рассмотри соседние пары типов bigram_i = (type_i, type_{i+1}) и посчитай частоты каждой биграммы (кортеж из двух строк).
Выведи топ-K строк в порядке убывания частоты; при равенстве частоты --> по лексикографическому порядку биграммы (first, second). Формат строки:

first>second: count

Если уникальных биграмм меньше K, выведи все. Если n < 2, вывода нет.

🧪 Пример 1 — ввод

8
08:00 PULSE
08:02 ARC
08:03 SPARK
08:06 PULSE
08:07 ARC
08:08 SPARK
08:09 SPARK
08:10 ARC
3

🧪 Пример 1 — вывод

PULSE>ARC: 2
ARC>SPARK: 2
SPARK>SPARK: 1
'''

# Считываем количество строк событий
n = int(input())

# Если событий меньше двух — невозможно образовать ни одной биграммы → выходим
if n < 2:
    exit()

# Считываем n строк, разбиваем каждую по пробелу и берём только второй элемент (тип события)
# Например, "08:00 PULSE" → берём "PULSE"
# Сохраняем все типы в список `events`
events = [input().split()[1] for _ in range(n)]

# Считываем число K — сколько топовых биграмм нужно вывести
k = int(input())

# Словарь для подсчёта частоты каждой биграммы
# Ключ: кортеж (type_i, type_{i+1}), значение: количество появлений
result = {}

# Проходим по всем парам соседних событий (от 0 до n-2)
for i in range(n - 1):
    one, two = events[i], events[i + 1]  # Текущее и следующее событие
    # Увеличиваем счётчик для биграммы (one, two)
    # Если такой биграммы ещё не было — начинаем с 0
    result[(one, two)] = result.get((one, two), 0) + 1

# Преобразуем словарь в список пар: (биграмма, частота)
output = result.items()

# Сортируем:
# - по частоте: убывание → -x[1]
# - при равной частоте: по лексикографическому порядку биграммы → x[0][0], затем x[0][1]
#   то есть сначала по первому элементу, потом по второму (лексикографически по возрастанию)
output = sorted(output, key=lambda x: (-x[1], x[0][0], x[0][1]))

# Выводим топ-K биграмм в нужном формате
for item, count in output[:k]:
    print(f'{item[0]}>{item[1]}: {count}')
