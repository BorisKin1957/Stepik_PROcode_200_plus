'''
🎯 Задание
Строка 1: слова через пробел.

Строка 2: буквы чужого алфавита через пробел, без повторов (например: θ α β γ).

Отсортируй слова лексикографически по данному алфавиту.
Критерии:

сравнение посимвольное по порядку букв в алфавите;

если одно слово является префиксом другого, короткое меньше;

буквы, отсутствующие в алфавите, считаются больше любых известных (идут в хвост),
порядок между ними сохраняется стабильностью.

Вывод: отсортированные слова в одну строку через пробел.
'''

# Считываем первую строку — список слов, разделяем по пробелам и убираем лишние пробелы
string = list(map(str.strip, input().split()))

# Считываем вторую строку — "чужой" алфавит, убираем пробелы между символами
alpha = input().replace(' ', '')

# Находим максимальную длину слова, чтобы выровнять все слова при сравнении
m = len(max(string, key=len))

# Максимальный индекс для неизвестных букв — они будут иметь ранг max_idx
max_idx = len(alpha)

# Список для хранения слов, содержащих хотя бы одну букву не из алфавита
unknow_words = []

# Проходим по каждому слову, чтобы проверить, содержит ли оно неизвестные буквы
for word in string:
    flag = True  # Флаг нужен, чтобы добавить слово в unknow_words только один раз
    for char in word:
        if flag:
            if char not in alpha:
                unknow_words.append(word)  # Добавляем слово, если есть неизвестная буква
                flag = False  # Блокируем повторное добавление
        else:
            continue

# Удаляем из основного списка все слова, которые содержат неизвестные буквы
string = [word for word in string if word not in unknow_words]

# Создаём список пар: (слово, список индексов длиной m, заполненный -m)
# Используется для представления каждого слова в виде последовательности рангов букв
idxes = [(word, [-m] * m) for word in string]

# Создаём словарь: символ → его порядковый номер в заданном алфавите
char_to_rank = {char: i for i, char in enumerate(alpha)}

# Преобразуем каждое слово в последовательность рангов букв
for i in range(len(string)):
    for j in range(len(string[i])):
        if string[i][j] in alpha:
            # Если буква в алфавите — берём её ранг
            idxes[i][1][j] = char_to_rank[string[i][j]]
        else:
            # Если буквы нет в алфавите — присваиваем ей ранг max_idx (самый большой)
            idxes[i][1][j] = max_idx

# Преобразуем список idxes в словарь: слово → список рангов
idx = dict(idxes)

# Сортируем слова по их спискам рангов (лексикографически)
# Слова с неизвестными буквами пока исключены
string = sorted(string, key=lambda x: idx[x])

# Добавляем в конец отсортированный основной список слова с неизвестными буквами
# Важно: порядок этих слов **не меняется** — это обеспечивает стабильность сортировки
string += unknow_words

# Выводим результат: слова через пробел
print(*string)