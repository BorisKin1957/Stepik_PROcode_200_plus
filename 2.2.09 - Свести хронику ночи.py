'''
Считай целое n. Затем считай n строк, в каждой два целых L R.
Объедини все интервалы, которые пересекаются или касаются общей границы
(например, [1, 2] и [2, 3] считаются одним окном).
Выведи объединённые интервалы по строкам в формате L R в порядке возрастания L.

🧪 Пример 1 ввод

3
1 3
3 5
6 6


🧪 Пример 1 вывод

1 5
6 6
'''


# Считываем количество интервалов
n = int(input())

# Считываем n строк, каждая содержит два числа — начало и конец интервала
# Преобразуем каждую строку в список из двух целых чисел
# Сортируем все интервалы по началу (по первому элементу)
lst = sorted([list(map(int, input().split())) for _ in range(n)])

# Список для хранения объединённых интервалов
result = []

# Берём первый интервал как текущий (с которым будем сравнивать остальные)
tmp = lst[0]

# Проходим по всем оставшимся интервалам, начиная со второго
for el in lst[1:]:
    # Если текущий интервал пересекается или касается следующего (el[0] <= tmp[1])
    # Например: [1,3] и [3,5] — касаются в точке 3, значит, объединяем
    if el[0] <= tmp[1]:
        # Если конец нового интервала больше, чем конец текущего — расширяем текущий интервал
        if el[1] >= tmp[1]:
            tmp = [tmp[0], el[1]]  # Обновляем конец интервала
        # Если el[1] < tmp[1], то ничего не делаем — текущий интервал уже покрывает новый
    else:
        # Если интервалы не пересекаются — добавляем текущий в результат
        result.append(tmp)
        # И начинаем новый текущий интервал
        tmp = el

# После цикла добавляем последний незавершённый интервал в результат
result.append(tmp)

# Выводим каждый объединённый интервал в формате "L R"
for segment in result:
    print(*segment)  # *segment распаковывает список: [1, 5] → "1 5"