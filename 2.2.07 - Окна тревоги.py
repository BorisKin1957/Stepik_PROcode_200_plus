'''
Считай строку целых чисел через пробел. Затем считай целое w.
Построй суммы всех подряд идущих отрезков длины w и выведи их в одной строке
через пробел. Если w <= 0 или w больше длины списка — выведи "Пусто".

🧪 Пример 1 ввод
1 3 2 4 5
3

🧪 Пример 1 вывод
6 9 11
'''


def get_sum_slice(lst: list[int], k: int) -> list[int]:
    '''Рекурсивная функция, возвращает список сумм всех последовательных подсписков длины k'''
    # Базовый случай: если длина текущего списка равна требуемой длине окна k,
    # возвращаем сумму всего списка как единственный элемент
    if len(lst) == k:
        return [sum(lst)]

    # Рекурсивный случай: берём сумму первых k элементов,
    # затем рекурсивно вызываем функцию для списка без первого элемента
    # и объединяем результаты
    return [sum(lst[:k])] + get_sum_slice(lst[1:], k)


# Считываем строку чисел, разбиваем по пробелам и преобразуем в список целых чисел
numbers = list(map(int, input().split()))

# Считываем ширину окна w
w = int(input())

# Проверяем допустимость значения w: должно быть положительным и не больше длины списка
if 0 < w <= len(numbers):
    # Если условие выполняется, выводим суммы окон длины w через пробел
    print(*get_sum_slice(numbers, w))
else:
    # Иначе — выводим "Пусто"
    print('Пусто')
