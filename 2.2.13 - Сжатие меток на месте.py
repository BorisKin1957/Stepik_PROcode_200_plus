'''Считай отсортированные элементы через пробел (строки/метки).
Удали повторы in-place: получи первые k уникальных элементов в начале списка.
Выведи на первой строке эти k элементов через пробел, на второй число k.
Новый список для результата создавать нельзя (используй перезапись в исходной
структуре). Если вход пуст выведи пустую строку и 0 на следующей.

🧪 Пример 1 ввод

a a b b b c d d


🧪 Пример 1 вывод

a b c d
4'''


result = input().split()


for sym in result:
    while result.count(sym) != 1:
        result.remove(sym)

print(*result)
print(len(result))
