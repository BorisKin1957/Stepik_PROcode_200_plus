'''
Считай целое k. Далее --> k строк:

ts dx dy

где ts, dx, dy --> целые числа; строки идут в порядке возрастания ts.

Нормализация направления:

    Если (dx,dy) == (0,0), нормализованный азимут --> (0,0).

    Иначе вычисли g = gcd(|dx|, |dy|), задай (nx,ny) = (dx/g, dy/g), затем приведи знак:
    если nx < 0 или (nx == 0 и ny < 0), умножь оба на -1.
    В итоге всегда nx > 0 или nx == 0 и ny > 0.

Сверни последовательность нормализованных азимутов в подряд идущие блоки одинаковых
значений (run-length encoding).
Выведи блоки в исходном порядке по одной строке в формате:

nx,ny x cnt

где cnt --> длина блока. Азимут (0,0) --> самостоятельный и не смешивается с другими.

🧪 Пример 1 — ввод

7
00 2 1
01 4 2
02 0 0
03 -2 -1
04 -4 -2
05 3 0
06 6 0


🧪 Пример 1 — вывод

2,1 x 2
0,0 x 1
2,1 x 2
1,0 x 2
'''


# Функция для вычисления наибольшего общего делителя (НОД) двух чисел
def get_gcd(a, b):
    # Берём минимальное из двух чисел — перебор будем вести от него вниз
    n = min(a, b)
    # Если одно из чисел ноль, НОД — это другое число (по определению)
    if n == 0:
        return max(a, b)
    # Перебираем все числа от n до 2 (включительно) в обратном порядке
    for i in range(n, 1, -1):
        # Если оба числа делятся на i без остатка — это и есть НОД
        if a % i == 0 and b % i == 0:
            return i
    # Если общий делитель не найден (кроме 1), возвращаем 1
    return 1


# Считываем количество строк данных
n = int(input())
# Список для хранения временных меток и нормализованных азимутов
azimuths = []

# Цикл по всем входным строкам
for _ in range(n):
    # Считываем временную метку ts и смещения dx, dy
    ts, dx, dy = map(int, input().split())

    # Если оба смещения нулевые — азимут (0, 0)
    if dx == dy == 0:
        azimuths.append([ts, (0, 0)])  # Сохраняем временную метку и азимут
    else:
        # Вычисляем НОД абсолютных значений dx и dy
        g = get_gcd(abs(dx), abs(dy))
        # Нормализуем вектор направления: делим на НОД
        nx, ny = int(dx / g), int(dy / g)

        # Приводим к стандартному виду: nx > 0, или если nx == 0, то ny > 0
        if nx < 0 or (nx == 0 and ny < 0):
            nx = -nx  # Инвертируем знак
            ny = -ny

        # Добавляем нормализованный азимут вместе с временной меткой
        azimuths.append([ts, (nx, ny)])

# Список для хранения результата (азимут и количество подряд идущих)
result = []
# Временная переменная: [текущий_азимут, счётчик]
tmp = [azimuths[0][1], 0]  # Начинаем с первого азимута, счётчик 0

# Обрабатываем каждый элемент (временная метка и азимут)
for cnt, xy in azimuths:
    # Если текущий азимут совпадает с предыдущим — увеличиваем счётчик
    if xy == tmp[0]:
        tmp[1] += 1
    else:
        # Если счётчик ещё 0 — значит, это первый элемент нового блока
        if tmp[1] == 0:
            result.append([xy, 1])  # Добавляем новый блок длины 1
        else:
            # Иначе сохраняем предыдущий блок
            result.append(tmp)
            # И начинаем новый блок
            tmp = [xy, 1]

# После цикла добавляем последний блок
result.append(tmp)

# Выводим результат в требуемом формате
for xy, count in result:
    x, y = xy  # Распаковываем координаты азимута
    print(f'{x},{y} x {count}')  # Форматируем как "nx,ny x cnt"