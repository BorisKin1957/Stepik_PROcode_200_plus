'''
Считай e, затем e строк:

u v

где u и v --> имена станций без пробелов. Известно, что каждая вершина имеет входящую
степень ≤ 1 и исходящую степень ≤ 1, граф --> набор непересекающихся цепочек без циклов.

Собери все максимальные цепи u1->u2->...->uk и выведи каждую цепь в отдельной строке:

u1->u2->...->uk

Цепи упорядочи по начальной вершине лексикографически. Одно ребро образует цепь длины 2.
Одиночных вершин (не встречающихся в рёбрах) во входе нет.

🧪 Пример 1 — ввод

5
A B
B C
E F
G H
C D

🧪 Пример 1 — вывод

A->B->C->D
E->F
G->H
'''


# Список для хранения всех рёбер графа (каждое ребро — это список [u, v])
all_tracks = []

# Считываем количество рёбер
e = int(input())

# Считываем e рёбер и добавляем их в список all_tracks
for _ in range(e):
    # Разбиваем строку на две части — начало и конец ребра — и сохраняем как список
    all_tracks.append(input().split())

# Сортируем все рёбра по начальной вершине (по лексикографическому порядку)
# Это нужно, чтобы в итоге цепи шли в правильном порядке
all_tracks.sort(key=lambda x: x[0])

# Результат — список цепей. Начинаем с первой отсортированной пары
result = [all_tracks[0]]

# Проходим по оставшимся рёбрам

for i in range(1, len(all_tracks)):
    track = all_tracks[i]  # Текущее ребро (список из двух станций: [u, v])
    last = result[-1][-1]  # Последняя станция последней цепи в результате

    if track[0] == last:  # Если начало текущего ребра продолжает конец последней цепи
        result[-1].append(track[1])  # Добавляем конечную станцию к последней цепи
    else:
        result.append(track)  # Иначе начинаем новую цепь

# Выводим каждую собранную цепь в формате "u1->u2->...->uk"
for track in result:
    print('->'.join(track))  # Объединяем станции цепи через "->" и печатаем
