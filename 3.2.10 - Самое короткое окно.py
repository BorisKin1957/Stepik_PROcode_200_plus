'''
Считай n, затем n строк неубывающего времени:

HH:MM type

где type --> строка без пробелов. Допускаются несколько событий в одну и ту же минуту.

Пусть множество типов это все различные type, встречающиеся во входе. Найди минимальный
по длительности (в минутах) непрерывный отрезок входной последовательности [L..R]
(по индексам), содержащий хотя бы по одному событию каждого типа.

Выведи одну строку:

Ltime-Rtime

где Ltime --> время на позиции L, Rtime --> время на позиции R (оба в формате HH:MM).
Если такого окна нет (например, n = 0), выведи Пусто.
'''

# Функция преобразует строку "HH:MM" в количество минут с начала суток
def to_minutes(hh_mm):
    h, m = map(int, hh_mm.split(':'))
    return h * 60 + m

# Функция преобразует количество минут обратно в строку "HH:MM"
def to_time(minutes):
    h, m = divmod(minutes, 60)
    return f"{h:02}:{m:02}"


events = []     # Список событий: будет содержать пары (время_в_минутах, тип_события)
types = set()   # Множество всех уникальных типов событий
times = set()   # Множество всех уникальных времён (в минутах)

n = int(input())

# Если нет событий — выводим "Пусто" и завершаем программу
if n == 0:
    print('Пусто')
    exit()

# Считываем события: время в минутах и тип; сохраняем уникальные типы и времена
for _ in range(n):
    HH_MM, event_type = input().split()
    time_in_minutes = to_minutes(HH_MM)
    events.append((time_in_minutes, event_type))
    types.add(event_type)
    times.add(time_in_minutes)

len_types = len(types)          # Общее количество уникальных типов событий
tms = sorted(times)             # Уникальные времена в порядке возрастания
len_tms = len(tms)

# # Сортируем события по времени на всякий случай (если ввод был неотсортирован)
# events.sort(key=lambda x: x[0])

all_cuts = []  # Список всех временных отрезков (в виде списков минут), содержащих все типы

# Перебираем начальные точки окна среди уникальных времён
for i in range(len_tms):
    flag = True  # Флаг для остановки внутреннего цикла при нахождении полного набора

    # Расширяем окно по количеству уникальных времён (j — длина среза от начала)
    for j in range(1, len(tms)):
        if flag:
            current = []           # Собираем типы событий, попавшие в текущий временной срез
            cut = tms[i:j]         # Текущий срез времён (в минутах) от i до j

            # Проверяем каждое событие: если его время входит в cut, добавляем его тип
            for tm, event in events:
                if tm in cut:
                    current.append(event)
                    # Если набрались все типы, фиксируем этот срез и выходим
                    if len(set(current)) == len_types:
                        all_cuts.append(cut)
                        flag = False  # Останавливаем расширение для текущего i
                        break

# Сортируем все найденные отрезки:
# 1. По длине (минимальная длительность по числу временных точек)
# 2. По начальному времени
# 3. По конечному времени
result = sorted(all_cuts, key=lambda x: (len(x), x[0], x[-1]))

# Выводим результат в формате HH:MM-HH:MM
print(f'{to_time(result[0][0])}-{to_time(result[0][-1])}')