'''Считай целое n, затем n целых чисел --> список кадров A. Затем считай целое
k (может быть отрицательным и по модулю больше n).
Циклически сдвинь список вправо на k позиций и выведи элементы через пробел.
Гарантируется n ≥ 1.'''

n = int(input())                    # Считываем количество элементов
numbers = list(map(int, input().split()))  # Считываем список чисел
k = int(input())                    # Считываем шаг сдвига

k = k % n                           # Определяем остаток от деления, если k > n (частичная нормализация)

for i in range(abs(k)):             # Повторяем abs(k) раз
    if k > 0:                       # Если сдвиг положительный — вправо
        numbers = [numbers[-1]] + numbers[:-1]  # Берём последний элемент и ставим в начало
    else:                           # Если сдвиг отрицательный — влево
        numbers = numbers[1:] + [numbers[0]]    # Берём первый элемент и ставим в конец

print(*numbers)                     # Выводим элементы списка через пробел

