'''
Считай целое n. Затем считай n строк: в каждой --> целые в неубывающем порядке
через пробел (пустая строка допустима, означает пустой список).
Слей все списки в один отсортированный список, выбирая на каждом шаге минимальный
головной элемент из непустых списков и удаляя его из головы соответствующего
списка (только операции списков: доступ к голове, удаление головы, добавление в результат).
Выведи числа в одну строку через пробел (если результатов нет, выведи пустую строку).
'''

# Считываем количество списков
n = int(input())

# m — общее количество чисел во всех списках
# numbers — список, содержащий только непустые списки (каждый — list[int])
m, numbers = 0, []

# Читаем n строк
for _ in range(n):
    # Преобразуем ввод в список целых чисел; если строка пустая — получим []
    lst = list(map(int, input().split()))

    # Увеличиваем счётчик общего количества чисел
    m += len(lst)

    # Если список не пустой, добавляем его в `numbers`
    if lst:
        numbers.append(lst)

# Если вообще нет чисел — завершаем программу (ничего не выводим)
if m == 0:
    exit()

# i — счётчик уже добавленных в результат элементов
# result — массив фиксированного размера для хранения результата
i, result = 0, [0] * m

# Основной цикл: пока не собрали все m элементов
while i < m:
    # Находим список с минимальным первым элементом
    min_num = min(numbers, key=lambda x: x[0])
    # Извлекаем первый элемент
    result[i] = min_num.pop(0)
    i += 1
    # Если список стал пустым — удаляем его
    if not min_num:
        numbers.remove(min_num)

# Выводим результат: все числа через пробел
print(*result)