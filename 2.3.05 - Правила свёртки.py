'''
Считай маршрут → слова через пробел.
Считай целое p → число правил.
Далее идут p строк вида:
pattern -> token, где pattern -- это слова через запятую и пробел
(например, Луг, Луг, Брод), а token -- одно слово-замена.

Как применять правила ("по кругам"):

Один круг -- это пройти все правила сверху вниз.

Для каждого правила делай один проход слева направо по маршруту и замени все
найденные неперекрывающиеся вхождения
pattern на token (через операции со срезами).

Если в течение круга была хотя бы одна замена, начинай новый круг снова с
первого правила.

Если за круг не было ни одной замены -- останавливаемся.

В конце выведи итоговый маршрут в одну строку через пробел.

Ограничения:
Разрешены только операции со списками/векторами и срезами
(сравнение подпоследовательности, присваивание срезу, вставка/удаление через срез).
Без словарей и множеств.
'''

# Считываем маршрут — строку слов, разделённых пробелами, и разбиваем её на список
route = input().split()

# Список для хранения правил в виде пар: (шаблон, замена)
patterns = []

# Считываем количество правил
for _ in range(int(input())):
    # Читаем строку вида "pattern -> token" и разделяем по стрелке
    left, right = input().split('->')

    # Преобразуем левую часть: разбиваем по запятым и убираем лишние пробелы вокруг каждого слова
    pattern = list(map(str.strip, left.split(',')))

    # Правая часть — это токен-замена; убираем пробелы в начале и конце
    token = right.strip()

    # Добавляем пару (шаблон, токен) в список правил
    patterns.append((pattern, token))

# Флаг, показывающий, была ли хоть одна замена в текущем "круге"
changed = True

# Основной цикл: продолжаем, пока в предыдущем круге происходили замены
while changed:
    # Перед началом нового круга сбрасываем флаг
    changed = False

    # Проходим по каждому правилу в порядке их ввода
    for pattern, token in patterns:
        k = len(pattern)  # длина шаблона (сколько слов нужно найти)
        i = 0  # индекс начала проверки в маршруте

        # Пока есть место для вхождения шаблона (осталось хотя бы k элементов)
        while i <= len(route) - k:
            # Проверяем, совпадает ли подпоследовательность route[i:i+k] с шаблоном
            if route[i:i + k] == pattern:
                # Если совпадает — заменяем этот срез на один элемент: [token]
                route[i:i + k] = [token]

                # Устанавливаем флаг, что была замена (это повлияет на продолжение цикла)
                changed = True

                # Пропускаем только что вставленный токен (чтобы не проверять его сразу же)
                # Это обеспечивает неперекрывающееся применение
                i += 1
            else:
                # Иначе просто двигаемся дальше
                i += 1

# Выводим итоговый маршрут как строку из слов, разделённых пробелами
print(*route)