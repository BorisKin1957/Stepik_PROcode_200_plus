'''
Считай температуры через пробел (целые). Для каждого элемента выведи число шагов
до следующего более тёплого значения. Если такого нет выведи 0. Ответ выведи в
 одну строку через пробел. Если вход пуст выведи пустую строку.

🧪 Пример 1 ввод

73 74 75 71 69 72 76 73


🧪 Пример 1 вывод

1 1 4 2 1 1 0 0
'''

# Считываем строку с числами, разделяем её по пробелам и преобразуем каждое значение в целое число
numbers = list(map(int, input().split()))

# Определяем длину списка температур
n = len(numbers)

# Создаём пустой список для хранения результата (количество дней до более тёплого)
result = []

# Проходим по каждому дню (по индексу i)
for i in range(n):
    # Флаг, чтобы отследить, нашли ли мы более тёплый день
    flag = True
    # Внутренний цикл: проверяем дни после текущего (начиная с i+1)
    for j in range(i + 1, n):
        if flag:  # Пока не нашли более тёплый день
            if numbers[i] >= numbers[j]:  # Если текущая температура >= следующей
                # Продолжаем искать, ничего не делаем
                if j == n - 1:  # Если это последний элемент и более тёплого не было
                    result.append(0)  # Записываем 0
                continue  # Переходим к следующему j
            else:
                # Нашли более тёплый день — записываем количество шагов (j - i)
                result.append(j - i)
                flag = False  # Выключаем флаг, чтобы больше не добавлять
        else:
            # Как только нашли более тёплый день, выходим из внутреннего цикла
            continue

    # Если мы на последнем элементе, то заведомо нет следующих дней
    if i == n - 1:
        result.append(0)

# Выводим результат: элементы списка через пробел
print(*result)


