'''Над городом дождь то льёт, то затихает. По форме крыш вокруг склада видно,
где вода будет скапливаться. Эти места Кастыль называет «ловушками дождя».
Они служат лучшим ориентиром: именно там культисты чаще всего ставят сигилы.

🎯 Задание
Считай высоты столбиков через пробел (целые числа, неотрицательные).
Выведи одно число --> суммарный объём «воды», которая может накопиться между
столбиками. Если вход пуст -- выведи 0.

🧪 Пример 1 ввод

0 1 0 2 1 0 1 3 2 1 2 1


🧪 Пример 1 вывод

6'''

levels = list(map(int, input().split()))
n = len(levels)
result = 0

if n == 0:
    print(0)
    exit()

# Массивы для максимальных высот слева и справа
left_max = [0] * n
right_max = [0] * n

left_max[0] = levels[0]
for i in range(1, n):
    left_max[i] = max(left_max[i - 1], levels[i])

right_max[n - 1] = levels[n - 1]
for i in range(n - 2, -1, -1):
    right_max[i] = max(right_max[i + 1], levels[i])

# Подсчёт объёма воды над каждым столбиком
for i in range(n):
    water_level = min(left_max[i], right_max[i])
    result += water_level - levels[i]

print(result)
