'''Считай целое n, затем n токенов --> строки без пробелов
(коды событий по кадрам).
Сверни последовательность в подряд идущие блоки одинаковых значений.
Для каждого блока выведи строку:

value:count

в порядке появления блоков. Гарантируется n ≥ 1.
'''

# Считываем количество элементов (n), но не используем его напрямую,
# так как дальше мы просто читаем строку и разбиваем её.
input()

# Считываем строку с токенами (например, "A A A B B C") и разбиваем её по пробелам на список
lst = input().split()

# Список для хранения результата — списков, каждый из которых представляет блок одинаковых элементов
result = []

# Временный список, в котором накапливается текущий блок одинаковых элементов
# Инициализируем его первым элементом из входного списка
tmp = [lst[0]]

# Проходим по всем элементам списка, начиная со второго (индекс 1)
for el in lst[1:]:
    # Если текущий элемент уже есть в текущем блоке (tmp)
    if el == tmp[0]:
        # Добавляем элемент в текущий блок
        tmp.append(el)
    else:
        # Если элемент отличается, значит, текущий блок закончился
        # Добавляем собранный блок в результат
        result.append(tmp)
        # Начинаем новый блок с текущего элемента
        tmp = [el]

# После цикла добавляем последний незавершённый блок в результат
result.append(tmp)

# Для каждого блока в результате выводим значение (первый элемент блока) и количество повторений
for token in result:
    print(f'{token[0]}:{len(token)}')
