'''
Цель: преобразовать список A к виду B, используя минимальные перемещения.
Для каждой позиции i:
- если A[i] != B[i], то найти элемент B[i] в A на позиции j > i,
- вытащить его (pop(j)), вставить на позицию i (insert(i, ...)),
- увеличить счётчик перемещений.
'''

# Чтение и обработка входных данных
A = list(map(str.strip, input().split(',')))
B = list(map(str.strip, input().split(',')))

moves = 0  # Счётчик количества перемещений

# Обрабатываем каждую позицию слева направо
for i in range(len(A)):
    if A[i] == B[i]:
        continue  # Элемент уже на месте

    j = A.index(B[i], i)  # Находим индекс элемента B[i] в A, начиная с i

    # Вытаскиваем элемент с позиции j и вставляем на позицию i
    elem = A.pop(j)
    A.insert(i, elem)
    moves += 1  # Учёт одного перемещения

# Вывод результата
print(*A, sep='\n')
print(f'Moves: {moves}')
