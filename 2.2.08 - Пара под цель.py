'''
Считай строку целых чисел через пробел. Затем считай целевое число t.
Найди индексы i < j (нумерация с 1), где a[i] + a[j] = t.
Выведи пару с лексикографически минимальными индексами (i, j).
Если подходящей пары нет — выведи None.
Решение читает из стандартного ввода и пишет в стандартный вывод.
'''

# Считываем строку, разбиваем её по пробелам и преобразуем каждую часть в целое число
numbers = list(map(int, input().split()))

# Считываем целевое число t
t = int(input())

# Получаем длину списка чисел
n = len(numbers)

# Создаём пустой список для хранения всех подходящих пар индексов (i+1, j+1)
result = []

# Внешний цикл: перебираем индексы i от 0 до n-1
for i in range(n):
    # Внутренний цикл: перебираем индексы j от i+1 до n-1 (чтобы i < j)
    for j in range(i + 1, n):
        # Проверяем, даёт ли сумма элементов на позициях i и j целевое значение t
        if numbers[i] + numbers[j] == t:
            # Если да, добавляем пару индексов (с учётом нумерации с 1) в результат
            result.append((i + 1, j + 1))

# Если нашлись подходящие пары
if result:
    # Находим лексикографически минимальную пару (min() работает именно так для кортежей)
    # И выводим её через пробел (распаковываем с помощью *)
    print(*min(result))
else:
    # Если пар нет — выводим None
    print(None)