'''
Считай целое n, затем n токенов --> строки без пробелов
(идентификаторы спектров), но между токенами есть пробел!
Удалите повторы, оставив только первое вхождение каждого значения.
Выведите уникальные значения в порядке первого появления, через пробел.
Если n = 0, выведите пустую строку.

Уточнения:

Сравнение регистрозависимое: Alpha и alpha --> разные метки.

Вход может содержать повторяющиеся значения произвольно.

🧪 Пример 1 — ввод

8
A B A C C B D D


🧪 Пример 1 — вывод

A B C D'''


n = int(input())

if n:
    tokens = [token.strip() for token in input().split()]

    result = []

    for token in tokens:
        if token not in result:
            result.append(token)

print(*result) if n else ''