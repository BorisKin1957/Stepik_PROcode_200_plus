'''
Задание:
- Первая строка: список меток, разделённых пробелами (может быть пустой).
- Вторая строка: целое число K.
- Вывод: вывести ровно K строк вида "метка: частота", отсортированных:
    1. По убыванию частоты.
    2. При равной частоте — по порядку первого появления во входе.
- Если уникальных меток меньше K — вывести все.
- Ограничения: разрешены только списки, векторы, срезы и линейные операции.
  Запрещено использовать словари, множества, кучи, библиотечные сортировки с компараторами.
'''

# Считываем первую строку, разбиваем по пробелам и убираем лишние пробелы вокруг каждого элемента
labels = list(map(str.strip, input().split()))

# Считываем второе число — количество топ-меток, которые нужно вывести
k = int(input())

# Получаем длину списка меток
n = len(labels)

# Если запрашиваемое K больше, чем общее количество меток, ограничиваем K числом меток
if k > n:
    k = n

# Создаём пустые списки:
# unique — для хранения уникальных меток (в порядке появления)
# counts — для хранения пар (метка, частота)
# result — для хранения результата (топ-K меток)
unique, counts, result = [], [], []

# Проходим по каждой метке из входного списка
for label in labels:
    # Если такая метка ещё не встречалась — добавляем её в unique
    if label not in unique:
        unique.append(label)
        # Добавляем пару (метка, количество вхождений) в counts
        # count() — линейная операция, разрешена по условию
        counts.append((label, labels.count(label)))

# Цикл для выбора K самых частых меток
for i in range(k):
    # Инициализируем максимальную частоту как 1 (минимально возможная)
    max_vol = 1
    # Временная переменная: берём первую метку как кандидата
    tmp = counts[0][0], max_vol

    # Проходим по всем элементам в counts, чтобы найти метку с наибольшей частотой
    for label, vol in counts:
        if vol > max_vol:
            max_vol = vol
            tmp = label, max_vol  # Обновляем кандидата

    # Добавляем найденную самую частую метку в результат
    result.append(tmp)

    # Удаляем эту метку из counts, чтобы в следующей итерации она не участвовала
    counts.remove(tmp)

# Выводим результат: каждую метку и её частоту в формате "метка: частота"
for label, vol in result:
    print(f'{label}: {vol}')
    