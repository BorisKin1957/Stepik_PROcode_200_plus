'''
Задание

Считай a, затем a строк смены A:

port HH:MM-HH:MM

затем b, затем b строк смены B в том же формате.

Для каждого port объединить окна обеих смен, склеить пересекающиеся или касающиеся
интервалы (считаем, что [s1,e1] и [s2,e2] склеиваются, если s2 ≤ e1), и вывести по портам
в алфавитном порядке. Внутри порта --> интервалы по возрастанию начала. Формат вывода:

port
HH:MM-HH:MM
HH:MM-HH:MM
...

Тест №2
Ввод

4
A 00:00-00:15
B 23:45-23:59
A 00:15-00:15
C 12:00-12:30
3
A 00:16-00:20
B 23:59-23:59
C 12:30-12:30

Вывод

A
00:00-00:15
00:16-00:20
B
23:45-23:59
C
12:00-12:30
'''


def get_data(lst):
    # Разбираем одну строку: например, ['A', '00:00-00:15']
    port = lst[0]  # Извлекаем название порта (например, 'A')
    times = lst[1].split('-')  # Делим время на начало и конец: ['00:00', '00:15']

    # Делим HH:MM на часы и минуты
    start = times[0].split(':')  # ['00', '00']
    end = times[1].split(':')  # ['00', '15']

    # Переводим время в минуты с начала суток (00:00 = 0, 23:59 = 1439)
    start_minutes = int(start[0]) * 60 + int(start[1])
    end_minutes = int(end[0]) * 60 + int(end[1])

    # Возвращаем кортеж: (порт, начало_в_минутах, конец_в_минутах)
    return port, start_minutes, end_minutes


# Список для хранения всех интервалов из обеих групп
data = []

# Цикл для двух групп ввода: сначала смена A (или первая группа), потом B (вторая)
for i in range(2):
    # Читаем количество строк в текущей группе
    n = int(input())

    # Считываем n строк и обрабатываем каждую
    for j in range(n):
        line = input().split()  # Например: ['A', '00:00-00:15']
        data.append(get_data(line))  # Преобразуем и добавляем в data

    # Сортируем данные по порту, а затем по времени начала
    data.sort(key=lambda x: (x[0], x[1]))

    # На первой итерации (i == 0) мы только читаем первую группу
    if i == 0:
        result = []  # Подготавливаем пустой результат для объединения
    else:
        # На второй итерации добавляем первую группу (из result) и вторую (data)
        data += result
        # Сортируем всё вместе
        data.sort(key=lambda x: (x[0], x[1]))
        result = []  # Обнуляем результат перед новым объединением

    # Основной цикл: объединение пересекающихся или касающихся интервалов
    while data:
        # Берём первый интервал
        result.append(data.pop(0))

        # Пока есть данные и следующий интервал — того же порта
        while data and data[0][0] == result[-1][0]:
            # Текущий последний интервал в result
            start_1, end_1 = result[-1][1], result[-1][2]
            # Следующий интервал из data
            start_2, end_2 = data[0][1], data[0][2]

            # Если интервалы пересекаются или касаются (start_2 <= end_1)
            if start_2 <= end_1:
                result.pop()  # Удаляем последний (он будет объединён)
                # Объединяем: начало остаётся start_1, конец — максимум из двух
                merged_end = max(end_1, end_2)
                result.append((data[0][0], start_1, merged_end))
            else:
                # Нет пересечения — просто добавляем новый интервал
                result.append((data[0][0], start_2, end_2))

            data.pop(0)  # Удаляем обработанный интервал из data

# Словарь для группировки результата по портам
outputs = {}

# Преобразуем минуты обратно в формат HH:MM
for port, start, end in result:
    str_start = f'{start // 60:02}:{start % 60:02}'  # Форматируем начало
    str_end = f'{end // 60:02}:{end % 60:02}'  # Форматируем конец
    # Добавляем строку интервала в список для данного порта
    outputs[port] = outputs.get(port, []) + [f'{str_start}-{str_end}']

# Выводим результат: порты в алфавитном порядке
for key in sorted(outputs.keys()):
    print(key)  # Название порта
    for val in outputs[key]:
        print(val)  # Каждый интервал



